-- Note on "relationships": rider_name / rider_phone are kept as
-- plain text fields (not a foreign key to rider_registrations),
-- matching the same pattern already used in customer_bookings.
-- This keeps assignment simple and avoids requiring every rider
-- to have a formal linked record before they can be assigned.


