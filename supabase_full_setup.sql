-- ================================================================
-- 1. CUSTOMER_BOOKINGS  (business/shop delivery bookings)
-- ================================================================
create table if not exists public.customer_bookings (
  id bigint generated by default as identity not null,
  created_at timestamp with time zone not null default now(),
  booking_id text null,
  shop_name text null,
  shop_address text null,
  shop_phone text null,
  customer_name text null,
  customer_phone text null,
  delivery_address text null,
  pickup_item text null,
  item_quantity text null,
  item_value numeric null,
  payment_status text null,
  payment_method text null,
  delivery_charge numeric null,
  booking_status text null default 'pending',
  rider_name text null,
  rider_phone text null,
  notes text null,
  constraint customer_bookings_pkey primary key (id)
);

create index if not exists idx_customer_bookings_booking_id on public.customer_bookings (booking_id);
create index if not exists idx_customer_bookings_status on public.customer_bookings (booking_status);
create index if not exists idx_customer_bookings_customer_phone on public.customer_bookings (customer_phone);
create index if not exists idx_customer_bookings_created_at on public.customer_bookings (created_at desc);


